package voxel;

public class Voxel
{
	private Float minX;
	private Float maxX;
	private Float minY;
	private Float maxY;
	private Float minZ;
	private Float maxZ;

	/**
	 * Default Constructor for {@link Voxel}
	 * 
	 * @param minX
	 *        The {@link Float} representing the minimum x value
	 * @param maxX
	 *        The {@link Float} representing the maximum x value
	 * @param minY
	 *        The {@link Float} representing the minimum y value
	 * @param maxY
	 *        The {@link Float} representing the maximum y value
	 * @param minZ
	 *        The {@link Float} representing the minimum z value
	 * @param maxZ
	 *        The {@link Float} representing the maximum x value
	 */
	public Voxel(Float minX, Float maxX, Float minY, Float maxY, Float minZ, Float maxZ)
	{
		this.minX = minX;
		this.maxX = maxX;
		this.minY = minY;
		this.maxY = maxY;
		this.minZ = minZ;
		this.maxZ = maxZ;
	}

	/**
	 * Gets a {@link Float} representing the minimum x value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMinX()
	{
		return minX;
	}

	/**
	 * Sets the minimum x value
	 * 
	 * @param minX
	 *        A {@link Float} representing the minimum x value
	 */
	public void setMinX(Float minX)
	{
		this.minX = minX;
	}

	/**
	 * Gets a {@link Float} representing the maximum x value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMaxX()
	{
		return maxX;
	}

	/**
	 * Sets the maximum x value
	 * 
	 * @param maxX
	 *        A {@link Float} representing the maximum x value
	 */
	public void setMaxX(Float maxX)
	{
		this.maxX = maxX;
	}

	/**
	 * Gets a {@link Float} representing the minimum y value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMinY()
	{
		return minY;
	}

	/**
	 * Sets the minimum y value
	 * 
	 * @param minY
	 *        A {@link Float} representing the minimum y value
	 */
	public void setMinY(Float minY)
	{
		this.minY = minY;
	}

	/**
	 * Gets a {@link Float} representing the maximum y value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMaxY()
	{
		return maxY;
	}

	/**
	 * Sets the maximum y value
	 * 
	 * @param maxY
	 *        A {@link Float} representing the maximum y value
	 */
	public void setMaxY(Float maxY)
	{
		this.maxY = maxY;
	}

	/**
	 * Gets a {@link Float} representing the minimum z value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMinZ()
	{
		return minZ;
	}

	/**
	 * Sets the minimum z value
	 * 
	 * @param minZ
	 *        A {@link Float} representing the minimum z value
	 */
	public void setMinZ(Float minZ)
	{
		this.minZ = minZ;
	}

	/**
	 * Gets a {@link Float} representing the maximum z value
	 * 
	 * @return A {@link Float}
	 */
	public Float getMaxZ()
	{
		return maxZ;
	}

	/**
	 * Sets the maximum z value
	 * 
	 * @param maxZ
	 *        A {@link Float} representing the maximum x value
	 */
	public void setMaxZ(Float maxZ)
	{
		this.maxZ = maxZ;
	}

}
